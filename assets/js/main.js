// Dès que le DOM est chargé
document.addEventListener('DOMContentLoaded', function () {

    // On récupère le div du slideshow
    const slideshowElement = document.getElementById('hero-slideshow');
    if (!slideshowElement){
        return;
    }

    // Récupération des images avec la classe slide
    const slideImages = slideshowElement.querySelectorAll('.slide');
    if (slideImages.length <= 1){
        return;
    }

    // index de banner actuel
    let currentSlideIndex = 0;

    // Delay entre chaque slide
    const slideDelayMs = 10420;

    /**
     * Permet d'afficher la slide suivante dans le slideshow
     */
    function showNextSlide() {

        // On retire la classe active de la slide courante
        slideImages[currentSlideIndex].classList.remove('active');

        // On la passe au prochain slide
        currentSlideIndex = (currentSlideIndex + 1) % slideImages.length;
        slideImages[currentSlideIndex].classList.add('active');
    }

    // On lance un intervalle pour switcher de slide toutes les 10420 secondes
    setInterval(showNextSlide, slideDelayMs);
});